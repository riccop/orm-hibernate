public class User{
	
	int id;
	String name;
	String phone;
	String address;
	String dayOfBirth;
	
	public getId(){return id;}
	public getName(){return name;}
	public getPhone(){return phone;}
	public getAddress(){return address;}
	public getDayOfBirth(){return dayOfBirth;}
	
	public setId(int id){return this.id=id;}
	public setName(){return this.name = name;}
	public setPhone(){return this.phone = phone;}
	public setAddress(){return this.address=address;}
	public setDayOfBirth(){return this.dayOfBirth = dayOfBirth;}
	
}